    options.SwaggerEndpoint("/swagger/v1/swagger.json", "NearExpiredProduct.API1");
    options.RoutePrefix = String.Empty;
});*/
app.UseHttpsRedirection();
app.UseAuthentication();
app.UseAuthorization();

app.MapControllers();

app.Run();
